package week1.day3;

public class ForLoop {

	public static void main(String[] args) {
		
		for (int i = 11; i <= 20; i++) {
			
			if (i%2 == 0) {
				
				System.out.println(i);
				
			}
			

	}
		
		System.out.println("-----");
		
         for (int i = 20; i >= 11; i--) {
			
			if (i%2 == 1) {
				
				System.out.println(i);
				
			}
			

	}

   }
}